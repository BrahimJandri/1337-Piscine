int	ft_sqrt(int nb)
{
	int	a;

	a = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (a <= 46340)
	{
		if (a * a == nb)
			return (a);
		a++;
	}
	return (0);
}
